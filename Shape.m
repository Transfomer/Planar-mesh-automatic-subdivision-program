%平面八节点的形函数
function y=Shape(s,t)
N=zeros(1,8);
ss=s*s;tt=t*t;
N(5)=0.5*(1-ss)*(1-t);
N(6)=0.5*(1-tt)*(1+s);
N(7)=0.5*(1-ss)*(1+t);
N(8)=0.5*(1-tt)*(1-s);
N(1)=0.25*(1-s)*(1-t)-0.5*N(5)-0.5*N(8);
N(2)=0.25*(1+s)*(1-t)-0.5*N(5)-0.5*N(6);
N(3)=0.25*(1+s)*(1+t)-0.5*N(6)-0.5*N(7);
N(4)=0.25*(1-s)*(1+t)-0.5*N(7)-0.5*N(8);
y=N; 
